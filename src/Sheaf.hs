-- | 'glueIfThenElse' is a complete characterisation of our sheaves. We could
-- have taken this as our definition of sheaves. I chose the definition in term
-- of covers because it can more easily be abstracted. So I use 'glueOracle' in
-- this file for things which can be proved generically for sheaves, and
-- 'glueIfThenElse' for things which only holds in our particular topology.
--
-- Note how the type of 'glueIfThenElse' is really isomorphic to the type of
-- 'glueOracle' with our choice of toplogy.
glueIfThenElse :: (Sheaf p) => Arith.Expr i Arith.TBool -> p i -> p i -> p i
glueIfThenElse b t f = glueOracle @_ @BoolCover b $ \c -> case c of
  TrueCovers -> t
  FalseCovers -> f

-- | A concrete property of our sheaves is that the fiber at 'Arith.TBool' is
-- completely determined by the fiber at 'Arit.TUnit'.
glueBool :: (Sheaf p) => (p 'Arith.TUnit, p 'Arith.TUnit) -> p 'Arith.TBool
glueBool (t, f) = glueIfThenElse Arith.Id (pb Arith.Unit t) (pb Arith.Unit f)

-- | This holds in every presheaf, but becomes an isomorphism (via 'glueBool')
-- in our sheaves.
splitBool :: (Presheaf p) => p 'Arith.TBool -> (p 'Arith.TUnit, p 'Arith.TUnit)
splitBool b = (pb Arith.BTrue b, pb Arith.BFalse b)

-- | Each element of @'Cover' i@ is the name of a cover of object @i@. In our
-- case we have only one: we can cover 'Arith.TBool' with arrows 'Arith.BTrue'
-- and 'Arith.BFalse'.
data Cover i where
  BoolCover :: Cover Arith.TBool

-- | Singleton types for 'Cover'.
type SCover :: Cover i -> Type
data SCover c where
  SBoolCover :: SCover BoolCover

class KnownCover c where
  knownCover :: SCover c

instance KnownCover BoolCover where
  knownCover = SBoolCover

-- | Maps each cover name to the type of its generators.
type Gen :: Cover i -> Type
data Gen c where
  TrueCovers :: Gen BoolCover
  FalseCovers :: Gen BoolCover

-- | Because we don't introduce any new colimits (that is, all the colimits that
-- we force in the sheaf categories are already colimits in the base category),
-- the representable presheaves (aka sheaves of the form @'Y' i@) are sheaves. A
-- site with the property that representables are sheaves is called
-- /subcanonical/.
--
-- Note that this is the one place where 'Arith.IfThenElse' is used, so this is
-- where we connect the control flow of the category of sheaves, and of the
-- arithmetic circuits.
instance Sheaf (Y i) where
  glueOracle @c k o = case knownCover @c of
    SBoolCover -> MkY $ Arith.IfThenElse k (unY (o TrueCovers)) (unY (o FalseCovers))

-- | Lifts if-then-else to sheaves. This is what we need sheaves for. Without
-- this Booleans are only evaluated after lowering to the base category,
-- preventing us from branching of @isZero@, say.
ifThenElseSh :: (Sheaf p) => Y 'Arith.TBool i -> p i -> p i -> p i
ifThenElseSh (MkY b) = glueIfThenElse b
